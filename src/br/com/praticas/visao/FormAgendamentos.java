    public void preencherTabela(String Sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"ID", "Paciente", "Turno", "MÃ©dico", "Data", "Status"};

        conex.conexao();
        conex.executarSql(Sql);

        try {
            conex.rs.first();
            do {
                dados.add(new Object[]{conex.rs.getInt("agenda_cod"), conex.rs.getString("paci_nome"), conex.rs.getString("agenda_turno"), conex.rs.getString("nome_medico"), conex.rs.getDate("agenda_data"), conex.rs.getString("agenda_status")});
            } while (conex.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao Listar Agendamentos!" + ex);
        }

        ModeloTabela modelo = new ModeloTabela(dados, colunas);

        jTableAgendamentos.setModel(modelo);
        jTableAgendamentos.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTableAgendamentos.getColumnModel().getColumn(0).setResizable(false);
        jTableAgendamentos.getColumnModel().getColumn(1).setPreferredWidth(240);
        jTableAgendamentos.getColumnModel().getColumn(1).setResizable(false);
        jTableAgendamentos.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTableAgendamentos.getColumnModel().getColumn(2).setResizable(false);
        jTableAgendamentos.getColumnModel().getColumn(3).setPreferredWidth(210);
        jTableAgendamentos.getColumnModel().getColumn(3).setResizable(false);
        jTableAgendamentos.getColumnModel().getColumn(4).setPreferredWidth(100);
        jTableAgendamentos.getColumnModel().getColumn(4).setResizable(false);
        jTableAgendamentos.getColumnModel().getColumn(5).setPreferredWidth(120);
        jTableAgendamentos.getColumnModel().getColumn(5).setResizable(false);
        jTableAgendamentos.getTableHeader().setReorderingAllowed(false);
        jTableAgendamentos.setAutoResizeMode(jTableAgendamentos.AUTO_RESIZE_OFF);
        jTableAgendamentos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        conex.desconecta();
    }

