    private void jToggleButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButtonSalvarActionPerformed
        if (jTextFieldNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o nome para continuar!" );
            jTextFieldNome.requestFocus();

        }else if(jFormattedTextFieldCRM.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o CRM para continuar!" );
            jFormattedTextFieldCRM.requestFocus();

        }else
        if (flag == 1){
            mod.setNome(jTextFieldNome.getText());
            mod.setEspecialidade((String) jComboBoxEspecializacao.getSelectedItem());
            mod.setCrm(Integer.parseInt(jFormattedTextFieldCRM.getText()));
            control.salvar(mod);

            jTextFieldNome.setText("");
            jFormattedTextFieldCRM.setText("");
            jTextFieldNome.setEnabled(false);
            jFormattedTextFieldCRM.setEnabled(false);
            jComboBoxEspecializacao.setEnabled(false);
            jToggleButtonSalvar.setEnabled(false);
            jToggleButtonCancelar.setEnabled(false);
            preencherTabela("select *from medicos order by nome_medico");
        }else{
            mod.setCodigo(Integer.parseInt(jTextFieldID.getText()));
            mod.setNome(jTextFieldNome.getText());
            mod.setEspecialidade((String)jComboBoxEspecializacao.getSelectedItem());
            mod.setCrm(Integer.parseInt(jFormattedTextFieldCRM.getText()));
            control.editar(mod);
            jTextFieldID.setText("");
            jTextFieldNome.setText("");
            jFormattedTextFieldCRM.setText("");

            jTextFieldNome.setEnabled(false);
            jFormattedTextFieldCRM.setEnabled(false);
            jComboBoxEspecializacao.setEnabled(false);
            jToggleButtonSalvar.setEnabled(false);
            jToggleButtonNovo.setEnabled(true);
            jToggleButtonCancelar.setEnabled(false);

            preencherTabela("select *from medicos order by nome_medico");

        }
    }//GEN-LAST:event_jToggleButtonSalvarActionPerformed

