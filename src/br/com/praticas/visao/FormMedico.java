    private void jToggleButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButtonPesquisarActionPerformed

        mod.setPesquisa(jTextFieldPesquisa.getText());
        BeansMedico model = control.buscaMedico(mod);
        jTextFieldNome.setText(model.getNome());
        jTextFieldID.setText(String.valueOf(model.getCodigo()));
        jFormattedTextFieldCRM.setText(String.valueOf(model.getCrm()));
        jComboBoxEspecializacao.setSelectedItem(model.getEspecialidade());

        preencherTabela("select *from medicos where nome_medico like'%"+mod.getPesquisa()+"%'");

    }//GEN-LAST:event_jToggleButtonPesquisarActionPerformed

    private void jTableMedicoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMedicoMouseClicked
        String nome_medico =""+jTableMedico.getValueAt(jTableMedico.getSelectedRow(), 1);
        conex.conexao();
        conex.executarSql("select *from medicos where nome_medico='"+nome_medico+"'");
        try {
            conex.rs.first();
            jTextFieldID.setText(String.valueOf(conex.rs.getInt("cod_medico")));
            jTextFieldNome.setText(conex.rs.getString("nome_medico"));
            jComboBoxEspecializacao.setSelectedItem(conex.rs.getString("especialidade_medico"));
            jFormattedTextFieldCRM.setText(conex.rs.getString("crm_medico"));

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao Selecionar Dados!");
        }
        conex.desconecta();
        jToggleButtonEditar.setEnabled(true);
        jToggleButtonExcluir.setEnabled(true);

    }//GEN-LAST:event_jTableMedicoMouseClicked

